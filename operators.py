# arithmetic operation
# +,-,*,/,%
a = 5
b = 4
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
# comparison operators
c = 34
d = 54
print(c>d)
print(c<d)
print(c==d)
print(c>=d)
print(c<=d)
print(c!=d)
# logical operators
# and,or,not
form1 = 34
form2 = 54
form3 = 78
form4 = 23
print(form1<form2 and form3>form4)
print(form1>form2 and form3<form4)
print(form1<form2 and form3<form4)
print(form1<form2 or form3>form4)
print(form1>form2 or form3<form4)
print(not(form1<form2 or form3<form4))
